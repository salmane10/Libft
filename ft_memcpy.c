#include "libft.h"

void	*ft_memcpy(void *dest, const void *src, size_t n)
{
	unsigned char		*d;
	const unsigned char	*s = src;

	d = dest;
	while (n-- > 0)
	{
		*d++ = *s++;
	}
	return (dest);
}
